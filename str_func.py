def upper_string(some_string):
    """Функция, получающая на вход строку и возвращающая её же
    со всеми заглавными буквами"""
    return some_string.upper()


def title_string(some_string):
    """Функция, принимающая на вход строку и возвращающая её же
    с первыми заглавными буквами слов"""
    return some_string.title()

